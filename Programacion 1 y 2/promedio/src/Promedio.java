public class Promedio {
    public static void main(String[] args) {
        // Definir los números
        int num1 = 2;
        int num2 = 3;
        int num3 = 5;
        int num4 = 6;

        // Calcular el promedio
        double promedio = (num1 + num2 + num3 + num4) / 4.0;

        // Mostrar el resultado
        System.out.println("El promedio es: " + promedio);
    }
}
